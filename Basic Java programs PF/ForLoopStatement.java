package com.technosys.datatypes;

public class ForLoopStatement {
	
	public static void main(String[] args) {
		//ini condition incre/decre
		// systax  for(init;condition;incre/decre){}
		//Nested for
		for(int i=0;i<=5;i++) //maintain no of rows needed
		{
			for(int j=0;j<=i;j++) //maintain no of columns
			{
				System.out.print(j+" "); //print the pattern
			}
			System.out.println();
		}
			
	}
	

}

