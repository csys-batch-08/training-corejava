package com.technosys.datatypes;

public class whileSample {
	
	public static void main(String[] args) {
		//initialization i=0, condition i<10 ,increment ++
		// syntax: while(expression){ stmts; }
		//System.out.println("*****");//to five times print the *
		int num=1; // initialization
		while(num<10)// condition
		{
			System.out.print("*\t");
			num++; //increment
		}
		
		
		
	}

}


