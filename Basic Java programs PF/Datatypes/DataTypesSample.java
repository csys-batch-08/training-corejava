package com.technosys.datatypes;

public class DataTypesSample {
	public static void main(String[] args) {

		// Premitive data types
		byte tinyNumber = 127; // range of -128 to 127
		short smallNumber = 12345; // range of -32768 to 32767
		int number = 1234567890; // range of -2^31 to 2^31 -1
		long largeNumber = 1234567890;// range of -2^63 to 2^63 -1
		float decimal = 1.11f;// range 7 decimal points
		double bigDecimal = 1.256; // range 15 decimal points
		char charecter = 'C'; // range 0 to 65685
		boolean flag = false; // values either true or false
		System.out.println("Byte Value is :" + tinyNumber);

	}
}
